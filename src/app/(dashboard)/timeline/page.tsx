export function buildTimelineReviewState(
  milestones: Array<{ isAutoGenerated: boolean }>
) {
  const pendingCount = milestones.filter(milestone => milestone.isAutoGenerated).length

  return {
    pendingCount,
    hasPending: pendingCount > 0,
  }
}

export function buildPendingGeneratedMilestoneIds(
  milestones: Array<{ id: string; isAutoGenerated: boolean }>
) {
  return milestones
    .filter(milestone => milestone.isAutoGenerated)
    .map(milestone => milestone.id)
}

export function buildTimelineSections<T extends { isAutoGenerated: boolean }>(milestones: T[]) {
  const pending = milestones.filter(milestone => milestone.isAutoGenerated)
  const confirmed = milestones.filter(milestone => !milestone.isAutoGenerated)

  return {
    pending,
    confirmed,
    hasPending: pending.length > 0,
  }
}

