function MilestoneCard({
  milestone: m,
  locale,
  editingId,
  setEditingId,
  handleUpdate,
  handleDelete,
  handleConfirmGenerated,
  onPreviewPhoto,
  inputClass,
  t,
  uiText,
}: {
  milestone: Milestone
  locale: string
  editingId: string | null
  setEditingId: (id: string | null) => void
  handleUpdate: (e: React.FormEvent<HTMLFormElement>, id: string) => Promise<void>
  handleDelete: (id: string) => Promise<void>
  handleConfirmGenerated: (id: string) => Promise<void>
  onPreviewPhoto: (photoId: string) => void
  inputClass: string
  t: ReturnType<typeof useTranslations<'TimelinePage'>>
  uiText: ReturnType<typeof buildTimelineUiText>
}) {
  const meta = buildTimelineMilestoneMeta({ isAutoGenerated: m.isAutoGenerated })
  const actionState = buildTimelineMilestoneActionState({ isAutoGenerated: m.isAutoGenerated })
  const readingMeta = buildTimelineReadingMeta(m, {
    withLocationAndPhoto: uiText.readingNarrativeWithLocationAndPhoto,
    withLocation: uiText.readingNarrativeWithLocation,
    withPhoto: uiText.readingNarrativeWithPhoto,
    generic: uiText.readingNarrativeGeneric,
  })

  return (
    <div className="relative pl-14">
      <div className={`absolute left-[18px] top-5 h-5 w-5 rounded-full border-2 ${
        m.isAutoGenerated
          ? 'border-info bg-info/20'
          : 'border-warm-accent bg-warm-accent/20'
      }`}
      />

      <div className="bg-warm-surface rounded-[var(--radius-lg)] border border-warm-border p-5">
        {editingId === m.id ? (
          <form onSubmit={e => handleUpdate(e, m.id)} className="space-y-3">
            <input name="title" defaultValue={m.title} required className={inputClass} />
            <textarea name="description" defaultValue={m.description || ''} rows={2} className={inputClass + ' resize-none'} />
            <div className="flex gap-3">
              <div className="flex-1">
                <VelvetDatePicker
                  ariaLabel={t('fieldDate')}
                  defaultValue={m.date.split('T')[0]}
                  locale={locale}
                  name="date"
                />
              </div>
              <input name="locationName" defaultValue={m.locationName || ''} placeholder={t('locationPlaceholder')} className={inputClass + ' flex-1'} />
            </div>
            <div className="flex gap-2">
              <Button type="submit" size="xs" variant="brand" leadingIcon={<RefreshIcon />}>{t('save')}</Button>
              <Button type="button" size="xs" variant="secondary" onClick={() => setEditingId(null)} leadingIcon={<XIcon />}>{t('cancel')}</Button>
            </div>
          </form>
        ) : (
          <div className="flex items-start justify-between gap-3">
            <div className="flex-1 min-w-0">
              <div className="flex items-center gap-2 mb-1">
                <h3 className="font-semibold text-warm-text">{m.title}</h3>
                {meta.showAiBadge ? (
                  <span className="px-1.5 py-0.5 text-[10px] text-info bg-info/10 rounded">
                    {t('aiGenerated')}
                  </span>
                ) : null}
              </div>
              <div className="mt-2 space-y-2">
                <p className="text-sm leading-6 text-warm-text/88">{readingMeta.narrativeText}</p>
              </div>
              {meta.needsReview ? (
                <p className="mt-2 text-sm text-info">
                  {uiText.recommendationReason}: {buildMilestoneRecommendationReason({
                    isAutoGenerated: m.isAutoGenerated,
                    locationName: m.locationName,
                    photoId: m.photoId,
                    copy: {
                      withLocationAndPhoto: uiText.recommendationReasonWithLocationAndPhoto,
                      withLocation: uiText.recommendationReasonWithLocation,
                      withPhoto: uiText.recommendationReasonWithPhoto,
                      generic: uiText.recommendationReasonGeneric,
                    },
                  })}
                </p>
              ) : null}
              <div className="flex flex-wrap gap-3 mt-3 text-xs text-warm-muted">
                <span>{new Date(m.date).toLocaleDateString(locale, {
                  year: 'numeric', month: 'long', day: 'numeric',
                })}
                </span>
                {m.locationName ? <span>{m.locationName}</span> : null}
                {m.photo ? <span>{uiText.relatedPhoto}</span> : null}
                {readingMeta.hasGeneratedNarrative ? (
                  <span className="inline-flex rounded-full bg-warm-bg px-2 py-0.5 text-[10px] font-medium uppercase tracking-[0.14em] text-warm-muted">
                    {uiText.narrativeFallbackLabel}
                  </span>
                ) : null}
              </div>
            </div>

            <div className="flex items-center gap-1 flex-shrink-0">
              {m.photo?.thumbnailUrl && m.photoId ? (
                <button
                  type="button"
                  onClick={() => onPreviewPhoto(m.photoId!)}
                  className="overflow-hidden rounded-[var(--radius-sm)] border border-warm-border/70 transition duration-200 hover:border-warm-accent/45"
                >
                  {/* eslint-disable-next-line @next/next/no-img-element */}
                  <img
                    src={m.photo.thumbnailUrl}
                    alt={m.title}
                    className="h-12 w-12 object-cover"
                  />
                </button>
              ) : null}
              {actionState.showConfirmAction ? (
                <Button
                  onClick={() => handleConfirmGenerated(m.id)}
                  size="xs"
                  variant="subtle"
                  title={uiText.confirmGenerated}
                >
                  {uiText.confirmGenerated}
                </Button>
              ) : null}
              {actionState.showIgnoreAction ? (
                <Button
                  onClick={() => handleDelete(m.id)}
                  size="xs"
                  variant="secondary"
                  title={uiText.ignoreGenerated}
                >
                  {uiText.ignoreGenerated}
                </Button>
              ) : null}
              <Button
                onClick={() => setEditingId(m.id)}
                size="xs"
                iconOnly
                variant="ghost"
                title={t('editTitle')}
                aria-label={t('editTitle')}
              >
                <EditIcon />
              </Button>
              {actionState.showDeleteAction ? (
                <Button
                  onClick={() => handleDelete(m.id)}
                  size="xs"
                  iconOnly
                  variant="ghost"
                  title={t('deleteTitle')}
                  aria-label={t('deleteTitle')}
                >
                  <TrashIcon />
                </Button>
              ) : null}
            </div>
          </div>
        )}
      </div>
    </div>
  )
}
